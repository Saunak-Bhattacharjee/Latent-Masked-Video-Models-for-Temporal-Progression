def decode_frames(video_path, max_frames=32, img_size=128):
    """
    Uniformly sample up to max_frames from a video via PyAV.
    Returns (T, 3, H, W) float32 tensor in [-1, 1], or None on failure.
    """
    frames = []
    try:
        container = av.open(str(video_path))
        stream    = container.streams.video[0]
        total     = stream.frames or 0
        stride    = max(1, total // max_frames) if total > 0 else 1
        for i, frame in enumerate(container.decode(video=0)):
            if total > 0 and i % stride != 0:
                continue
            img = frame.to_image().convert('RGB')
            img = img.resize((img_size, img_size), Image.BILINEAR)
            arr = np.array(img, dtype=np.float32) / 127.5 - 1.0
            frames.append(arr)
            if len(frames) >= max_frames:
                break
        container.close()
    except Exception:
        return None
    if len(frames) < 2:
        return None
    return torch.tensor(np.stack(frames).transpose(0, 3, 1, 2))  # (T,3,H,W)


