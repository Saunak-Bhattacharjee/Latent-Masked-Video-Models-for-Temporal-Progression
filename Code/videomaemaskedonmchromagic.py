@torch.no_grad()
def extract_unmasked_results(loader):
    out = defaultdict(list)
    for batch in loader:
        ctx = batch['ctx_tokens'].to(DEVICE)  # (B, CTX_N, D)
        tgt = batch['tgt_tokens'].to(DEVICE)  # (B, TGT_N, D)
        B, Nt, D = tgt.shape

        future_pred = predictor_unmasked(ctx)   # (B, TGT_N, D)

        # Normalised future residual
        fp_n = F.normalize(future_pred.reshape(B*Nt, D), dim=-1).reshape(B, Nt, D)
        ft_n = F.normalize(tgt.reshape(B*Nt, D),          dim=-1).reshape(B, Nt, D)
        future_res = (fp_n - ft_n).norm(dim=-1).mean(dim=-1)   # (B,) ∈ [0,2]

        # Directional cosine similarity and improvement
        ctx_mean  = ctx.mean(dim=1)
        tgt_mean  = tgt.mean(dim=1)
        pred_mean = future_pred.mean(dim=1)
        actual_dir = tgt_mean - ctx_mean
        pred_dir   = pred_mean - ctx_mean
        future_cos  = F.cosine_similarity(pred_dir, actual_dir, dim=-1)
        copy_cos    = F.cosine_similarity(
            torch.zeros_like(ctx_mean), actual_dir, dim=-1
        )
        improvement = future_cos - copy_cos

        out['ctx_r'].append(ctx_mean.cpu().float().numpy())
        out['future_res'].append(future_res.cpu().numpy())
        out['future_cos'].append(future_cos.cpu().numpy())
        out['copy_cos'].append(copy_cos.cpu().numpy())
        out['improvement'].append(improvement.cpu().numpy())
        out['main_cat'].append(batch['main_cat'].numpy())
        out['sub_cat'].append(np.array(batch['sub_cat']))

    return {k: np.concatenate(v) for k, v in out.items()}


