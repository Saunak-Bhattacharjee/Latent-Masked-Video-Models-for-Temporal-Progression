@torch.no_grad()
def encode_video(video_path):
    """
    Returns (N_TOK, ENC_D) float32 token sequence, or None on failure.
    """
    frames = decode_clip(video_path, n_frames=N_FRAMES, img_size=IMG_SIZE)
    if frames is None: return None

    inputs = processor(frames, return_tensors='pt')
    pv     = inputs['pixel_values'].to(DEVICE)   # (1, T, C, H, W)

    out    = vmae(pixel_values=pv)
    tokens = out.last_hidden_state.squeeze(0).cpu().float()  # (N_TOK, D)
    return tokens


