def decode_clip(video_path, n_frames=16, img_size=224):
    """
    Uniformly sample n_frames from a video.
    Returns list of PIL RGB Images (required by VideoMAEImageProcessor),
    or None on failure.
    """
    frames = []
    try:
        container = av.open(str(video_path))
        stream    = container.streams.video[0]
        total     = stream.frames or 0
        stride    = max(1, total // n_frames) if total > 0 else 1
        for i, frame in enumerate(container.decode(video=0)):
            if total > 0 and i % stride != 0: continue
            img = frame.to_image().convert('RGB').resize(
                (img_size, img_size), Image.BILINEAR
            )
            frames.append(img)
            if len(frames) >= n_frames: break
        container.close()
    except Exception:
        return None

    while len(frames) < n_frames:
        frames.append(frames[-1])
    return frames[:n_frames]   # list of n_frames PIL Images


