@torch.no_grad()
def extract_one_per_video(feat_cache_split, predictor, device):
    """
    One representation per video — no repeated sampling.
    Uses a fixed deterministic mask (first N_MASK tokens masked).
    Returns X (n_videos, D), y_main, y_sub, videoids
    """
    X, y_main, y_sub, vids = [], [], [], []

    for e in feat_cache_split:
        tok = e['tokens']   # (N_TOK, D)

        ctx_all = tok[:CTX_N]   # (CTX_N, D)
        tgt_all = tok[CTX_N:]   # (TGT_N, D)

        vis_ids  = torch.arange(N_MASK, CTX_N)    # (N_VIS,)
        mask_ids = torch.arange(0, N_MASK)         # (N_MASK,)

        vis_tok  = ctx_all[vis_ids].unsqueeze(0).to(device)    # (1, N_VIS, D)
        vis_ids_ = vis_ids.unsqueeze(0).to(device)             # (1, N_VIS)
        msk_ids_ = mask_ids.unsqueeze(0).to(device)            # (1, N_MASK)

        _, future_pred = predictor(vis_tok, vis_ids_, msk_ids_)
        rep = future_pred.mean(dim=1).squeeze(0).cpu().float().numpy()  # (D,)

        X.append(rep)
        y_main.append(e['main_cat_idx'])
        y_sub.append(e['sub_cat'])
        vids.append(e['videoid'])

    return np.array(X), np.array(y_main), y_sub


