def latent_mask_loss(recon_pred, recon_tgt,
                      future_pred, future_tgt,
                      lam_recon=1.0, lam_future=1.0):
    """
    recon_pred, recon_tgt   : (B, N_MASK, D)
    future_pred, future_tgt : (B, TGT_N, D)
    """
    B, Nm, D = recon_pred.shape
    Bt, Nt, _ = future_pred.shape

    # Flatten and normalise
    rp = F.normalize(recon_pred.reshape(B*Nm, D),  dim=-1)
    rt = F.normalize(recon_tgt.reshape(B*Nm, D),   dim=-1)
    fp = F.normalize(future_pred.reshape(Bt*Nt, D), dim=-1)
    ft = F.normalize(future_tgt.reshape(Bt*Nt, D),  dim=-1)

    recon_loss  = F.mse_loss(rp, rt)
    future_loss = F.mse_loss(fp, ft)

    total = lam_recon * recon_loss + lam_future * future_loss
    return total, {'recon': recon_loss.item(), 'future': future_loss.item()}

