def lr_at(step):
    if step < warmup_steps:
        return BASE_LR * step / max(1, warmup_steps)
    p = (step - warmup_steps) / max(1, total_steps - warmup_steps)
    return 1e-6 + (BASE_LR - 1e-6) * 0.5 * (1 + math.cos(math.pi * p))


