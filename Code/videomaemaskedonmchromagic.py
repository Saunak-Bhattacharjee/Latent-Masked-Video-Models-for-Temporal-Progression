class UnmaskedVideoDataset(Dataset):
    """
    Each sample:
      ctx_tokens : (CTX_N, D)  ALL context tokens — nothing hidden
      tgt_tokens : (TGT_N, D)  target frame tokens
      main_cat   : int
    """

    def __init__(self, cache, samples_per_video=8):
        self.cache             = cache
        self.samples_per_video = samples_per_video
        self._build_index()

    def _build_index(self):
        self.samples = []
        for vi in range(len(self.cache)):
            for _ in range(self.samples_per_video):
                self.samples.append(vi)
        random.shuffle(self.samples)

    def __len__(self): return len(self.samples)

    def __getitem__(self, idx):
        vi  = self.samples[idx]
        e   = self.cache[vi]
        tok = e['tokens']          # (N_TOK, D)
        return {
            'ctx_tokens': tok[:CTX_N],     # (CTX_N, D) — full context
            'tgt_tokens': tok[CTX_N:],     # (TGT_N, D)
            'main_cat'  : e['main_cat_idx'],
            'sub_cat'   : e['sub_cat'],
        }


