class PositionalEncoding(nn.Module):
    def __init__(self, d_model, max_len=2048):
        super().__init__()
        pe  = torch.zeros(max_len, d_model)
        pos = torch.arange(max_len).unsqueeze(1).float()
        div = torch.exp(torch.arange(0, d_model, 2).float() *
                        (-math.log(10000.0) / d_model))
        pe[:, 0::2] = torch.sin(pos * div)
        pe[:, 1::2] = torch.cos(pos * div[:d_model//2])
        self.register_buffer('pe', pe)   # (max_len, d)

    def forward(self, ids):
        """ids: (B,N) position indices"""
        return self.pe[ids, :]


class LatentMaskPredictor(nn.Module):

    def __init__(self, enc_dim, pred_dim, n_heads, n_layers,
                 n_vis, n_mask, n_tgt):
        super().__init__()
        self.n_vis   = n_vis
        self.n_mask  = n_mask
        self.n_tgt   = n_tgt
        self.pred_dim = pred_dim

        # Input projection
        self.in_proj = nn.Linear(enc_dim, pred_dim)

        # Learnable tokens for masked positions and future positions
        self.mask_token   = nn.Parameter(torch.randn(1, 1, pred_dim) * 0.02)
        self.future_token = nn.Parameter(torch.randn(1, 1, pred_dim) * 0.02)

        # Positional encoding over the full sequence
        self.pos_enc = PositionalEncoding(pred_dim, max_len=CTX_N + TGT_N + 16)

        # Shared transformer encoder (attends globally over all tokens)
        encoder_layer = nn.TransformerEncoderLayer(
            d_model=pred_dim, nhead=n_heads,
            dim_feedforward=pred_dim * 4,
            dropout=0.0, batch_first=True, norm_first=True
        )
        self.transformer = nn.TransformerEncoder(encoder_layer,
                                                  num_layers=n_layers)
        self.norm = nn.LayerNorm(pred_dim)

        # Separate output heads for reconstruction and future prediction
        self.recon_head  = nn.Linear(pred_dim, enc_dim)
        self.future_head = nn.Linear(pred_dim, enc_dim)

        self._init_weights()

    def _init_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.trunc_normal_(m.weight, std=0.02)
                if m.bias is not None: nn.init.zeros_(m.bias)

    def forward(self, vis_tokens, vis_ids, mask_ids):
        """
        vis_tokens : (B, N_VIS, enc_dim)  — visible context tokens
        vis_ids    : (B, N_VIS)           — their sequence positions
        mask_ids   : (B, N_MASK)          — masked positions (to reconstruct)

        Returns:
          recon_pred  : (B, N_MASK, enc_dim)  predicted masked context tokens
          future_pred : (B, N_TGT,  enc_dim)  predicted target frame tokens
        """
        B = vis_tokens.shape[0]

        # 1. Project visible tokens and add positional encoding
        vis = self.in_proj(vis_tokens)              # (B, N_VIS, pred_dim)
        vis = vis + self.pos_enc(vis_ids)           # Correctly get and add position

        # 2. Masked context tokens: learnable token + positional encoding
        mask_tok = self.mask_token.expand(B, self.n_mask, -1).clone()
        mask_tok = mask_tok + self.pos_enc(mask_ids) # Correctly get and add position

        # 3. Future tokens: learnable token + offset positional encoding
        future_positions = torch.arange(
            CTX_N, CTX_N + self.n_tgt, device=vis_tokens.device
        ).unsqueeze(0).expand(B, -1) # (B, TGT_N)
        fut_tok = self.future_token.expand(B, self.n_tgt, -1).clone()
        fut_tok = fut_tok + self.pos_enc(future_positions) # Correctly get and add position

        # 4. Concatenate all tokens: [visible | masked | future]
        x = torch.cat([vis, mask_tok, fut_tok], dim=1)  # (B, N_VIS+N_MASK+TGT_N, d)

        # 5. Transformer (global attention)
        x = self.norm(self.transformer(x))

        # 6. Read out predictions at the correct positions
        recon_out  = x[:, self.n_vis : self.n_vis + self.n_mask, :]  # masked ctx
        future_out = x[:, self.n_vis + self.n_mask:, :]              # future

        return self.recon_head(recon_out), self.future_head(future_out)


