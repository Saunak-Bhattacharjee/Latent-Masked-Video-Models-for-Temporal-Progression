class UnmaskedTemporalPredictor(nn.Module):
    """
    Input  : full context tokens (CTX_N, D)
    Output : predicted target frame tokens (TGT_N, D)

    Architecture mirrors LatentMaskPredictor but without mask/recon tokens.
    Uses the same pred_dim, n_heads, n_layers for a fair comparison.
    """

    def __init__(self, enc_dim, pred_dim, n_heads, n_layers, ctx_n, tgt_n):
        super().__init__()
        self.ctx_n   = ctx_n
        self.tgt_n   = tgt_n

        self.in_proj = nn.Linear(enc_dim, pred_dim)

        # Learnable future query tokens — one per target position
        self.future_token = nn.Parameter(torch.randn(1, tgt_n, pred_dim) * 0.02)

        # Positional encoding over full sequence
        self.pos_enc = PositionalEncoding(pred_dim, max_len=ctx_n + tgt_n + 16)

        encoder_layer = nn.TransformerEncoderLayer(
            d_model=pred_dim, nhead=n_heads,
            dim_feedforward=pred_dim * 4,
            dropout=0.0, batch_first=True, norm_first=True
        )
        self.transformer = nn.TransformerEncoder(encoder_layer, num_layers=n_layers)
        self.norm        = nn.LayerNorm(pred_dim)
        self.future_head = nn.Linear(pred_dim, enc_dim)

        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.trunc_normal_(m.weight, std=0.02)
                if m.bias is not None: nn.init.zeros_(m.bias)

    def forward(self, ctx_tokens):
        B   = ctx_tokens.shape[0]
        pe  = self.pos_enc.pe                # (max_len, d)

        ctx_pos = torch.arange(self.ctx_n, device=ctx_tokens.device)
        fut_pos = torch.arange(self.ctx_n, self.ctx_n + self.tgt_n,
                              device=ctx_tokens.device)

        ctx = self.in_proj(ctx_tokens) + pe[ctx_pos].unsqueeze(0)   # (B, CTX_N, d)

        fut = self.future_token.expand(B, -1, -1).clone()
        fut = fut + pe[fut_pos].unsqueeze(0)                         # (B, TGT_N, d)

        x = torch.cat([ctx, fut], dim=1)
        x = self.norm(self.transformer(x))

        return self.future_head(x[:, self.ctx_n:, :])
