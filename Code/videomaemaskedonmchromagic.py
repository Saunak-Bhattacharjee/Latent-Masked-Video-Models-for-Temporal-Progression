def future_only_loss(future_pred, future_tgt):
    B, Nt, D = future_pred.shape
    fp = F.normalize(future_pred.reshape(B*Nt, D), dim=-1)
    ft = F.normalize(future_tgt.reshape(B*Nt, D),  dim=-1)
    loss = F.mse_loss(fp, ft)
    return loss, {'future': loss.item()}


N_EPOCHS_ABL  = 60
BASE_LR_ABL   = 1e-4
WEIGHT_DECAY  = 1e-4
WARMUP_EPOCHS = 8

optimizer_abl   = torch.optim.AdamW(
    predictor_unmasked.parameters(),
    lr=BASE_LR_ABL, weight_decay=WEIGHT_DECAY
)
total_steps_abl  = N_EPOCHS_ABL * len(train_loader_unmasked)
warmup_steps_abl = WARMUP_EPOCHS * len(train_loader_unmasked)

def lr_abl(step):
    if step < warmup_steps_abl:
        return BASE_LR_ABL * step / max(1, warmup_steps_abl)
    p = (step - warmup_steps_abl) / max(1, total_steps_abl - warmup_steps_abl)
    return 1e-6 + (BASE_LR_ABL - 1e-6) * 0.5 * (1 + math.cos(math.pi * p))


