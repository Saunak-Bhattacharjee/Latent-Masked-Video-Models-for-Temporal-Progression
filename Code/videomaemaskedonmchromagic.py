class LatentMaskVideoDataset(Dataset):
    """
    Each sample:
      vis_tokens  : (N_VIS, D)  — visible (unmasked) context tokens
      vis_ids     : (N_VIS,)    — their positions in the full sequence
      mask_tokens : (N_MASK, D) — masked context tokens (reconstruction targets)
      mask_ids    : (N_MASK,)   — their positions
      tgt_tokens  : (TGT_N, D)  — all target frame tokens (future prediction targets)
      main_cat    : int
    """

    def __init__(self, cache, samples_per_video=8):
        self.cache            = cache
        self.samples_per_video = samples_per_video
        self._build_index()

    def _build_index(self):
        self.samples = []
        for vi in range(len(self.cache)):
            for _ in range(self.samples_per_video):
                self.samples.append(vi)
        random.shuffle(self.samples)

    def __len__(self): return len(self.samples)

    def __getitem__(self, idx):
        vi      = self.samples[idx]
        e       = self.cache[vi]
        tokens  = e['tokens']   # (N_TOK, D)

        # Split into context and target along temporal axis
        ctx_all = tokens[:CTX_N]   # (CTX_N, D)
        tgt_all = tokens[CTX_N:]   # (TGT_N, D)

        # Random latent mask on context tokens
        perm     = torch.randperm(CTX_N)
        vis_ids  = perm[N_MASK:].sort().values   # (N_VIS,)
        mask_ids = perm[:N_MASK].sort().values   # (N_MASK,)

        vis_tokens  = ctx_all[vis_ids]    # (N_VIS, D)
        mask_tokens = ctx_all[mask_ids]   # (N_MASK, D)  ground truth for reconstruction

        return {
            'vis_tokens'  : vis_tokens,
            'vis_ids'     : vis_ids,
            'mask_tokens' : mask_tokens,
            'mask_ids'    : mask_ids,
            'tgt_tokens'  : tgt_all,
            'main_cat'    : e['main_cat_idx'],
            'sub_cat'     : e['sub_cat'],
        }


